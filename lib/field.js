// FIXME: feature test for Proxy; if it doesn't exist, back off to a trigger with a mangled name


if (!("Handler" in Proxy)) {
    Proxy.Handler = function(obj) {
        // Handler copied from
        // http://wiki.ecmascript.org/doku.php?id=harmony:proxies&s=proxy%20object#examplea_no-op_forwarding_proxy
        return {
            getOwnPropertyDescriptor: function(name) {
                var desc = Object.getOwnPropertyDescriptor(obj, name);

                // a trapping proxy's properties must always be configurable
                desc.configurable = true;
                return desc;
            },
            getPropertyDescriptor: function(name) {
                var desc = getPropertyDescriptor(obj, name);

                // a trapping proxy's properties must always be configurable
                desc.configurable = true;
                return desc;
            },
            getOwnPropertyNames: function() {
                return Object.getOwnPropertyNames(obj);
            },
            defineProperty: function(name, desc) {
                Object.defineProperty(obj, name, desc);
            },
            "delete": function(name) { return delete obj[name]; },
            fix: function() {
                if (Object.isFrozen(obj)) {
                    return getOwnProperties(obj);
                }

                // As long as obj is not frozen, the proxy won't allow itself to be fixed.
                return undefined; // will cause a TypeError to be thrown
            },

            has: function(name) { return name in obj; },
            hasOwn: function(name) { return ({}).hasOwnProperty.call(obj, name); },
            get: function(receiver, name) { return obj[name]; },

            // bad behavior when set fails in non-strict mode
            set: function(receiver, name, val) { obj[name] = val; return true; },
            enumerate: function() {
                var result = [];
                for (name in obj) { result.push(name); };
                return result;
            },
            keys: function() { return Object.keys(obj); }
        };
    };
}

function PrivateField() {

    var outbox;

    function forward(obj, internals) {
        var handler = Proxy.Handler(obj);
        var has = handler.has;
        handler.has = function(name) {
            outbox = internals;
            return has(name);
        };
        return handler;
    }

    function expose(obj) {
        if ((typeof obj !== "object" && typeof obj !== "function") || obj === null)
            return null;

        "private" in obj;
        var result = outbox;
        outbox = null;
        return result;
    }

    return {
        wrap: function(obj, internals, proto) {
            return Proxy.create(forward(obj, internals), proto || Object.getPrototypeOf(obj));
        },
        wrapFunction: function(obj, internals, call, construct) {
            return Proxy.createFunction(forward(obj, internals), call, construct);
        },
        unwrap: function(obj) {
            return expose(obj);
        }
    };

}
